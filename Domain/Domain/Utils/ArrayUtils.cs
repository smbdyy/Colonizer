namespace Domain.Utils;

public static class ArrayUtils
{
    public static void FillTwoDimArray<T>(T[,] array, T value)
    {
        for (int i = 0; i < array.GetLength(0); i++)
        {
            for (int j = 0; j < array.GetLength(1); j++)
            {
                array[i, j] = value;
            }
        }
    }
}